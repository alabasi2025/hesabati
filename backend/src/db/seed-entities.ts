/**
 * seed-entities.ts — توثيق مرجعي
 *
 * يوثّق هذا الملف أنماط بناء البيانات الأولية المستخدمة في seed.ts
 * للتشغيل الفعلي: pnpm -C backend run db:seed
 *
 * ══════════════════════════════════════════════════════
 * نمط الكيانات المالية (صناديق / بنوك / محافظ / صرافين)
 * ══════════════════════════════════════════════════════
 *
 * كل كيان مالي يتكوّن من طبقتين:
 *
 * 1) حساب في دليل الحسابات (accounts)
 *    - كود: FND-01, BNK-01, WLT-01 ... (مولّد تلقائياً)
 *    - ledgerCode: 001-001-001 (XXX-YYY-ZZZ) — حساب تحليلي
 *
 * 2) الكيان التشغيلي (funds / banks / wallets / exchanges)
 *    - كود مركّب: {كود الحساب}/{رقم فرعي}
 *    - مثال: FND-01/1 (أول صندوق تحت حساب FND-01)
 *    -        FND-01/2 (ثانٍ تحت نفس الحساب)
 *    - يُتيح ربط أكثر من كيان بنفس الحساب
 *
 * ══════════════════════════════════════════════════════
 * نمط الكيانات المجمّعة (شركاء / موظفون / مخازن / عهد)
 * ══════════════════════════════════════════════════════
 *
 * 1) حساب تحكم مشترك لكل العمل
 *    - مثال: PRT-01 (حسابات الشركاء)، EMP-01 (حسابات الموظفين)
 *
 * 2) حساب تحليلي خاص بكل فرد (XXX-YYY-ZZZ)
 *    - مثال: حساب المالك، حساب الشريك محمد
 *
 * 3) كود الكيان: {كود الحساب التحكم}/{رقم فرعي}
 *    - مثال: PRT-01/1, PRT-01/2, PRT-01/3
 *
 * ══════════════════════════════════════════════════════
 * نمط الموردين
 * ══════════════════════════════════════════════════════
 *
 * 1) حساب تحكم لكل نوع مورد (SUP-01 موردو الديزل، SUP-02 موردو الزيوت ...)
 * 2) حساب تحليلي لكل مورد
 * 3) كود المورد: {كود حساب النوع}/{رقم فرعي}
 *    - مثال: SUP-01/1 (أول مورد ديزل)، SUP-02/1 (أول مورد زيوت)
 */